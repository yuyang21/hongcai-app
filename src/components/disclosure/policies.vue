      <p class="text-center">第一章 总则</p>
      <p>第一条　为了预防洗钱活动，维护金融秩序，遏制洗钱犯罪及相关犯罪，制定本法。</p>
      <p>第二条　第二条　本法所称反洗钱，是指为了预防通过各种方式掩饰、隐瞒毒品犯罪、黑社会性质的组织犯罪、恐怖活动犯罪、走私犯罪、贪污贿赂犯罪、破坏金融管理秩序犯罪、金融诈骗犯罪等犯罪所得及其收益的来源和性质的洗钱活动，依照本法规定采取相关措施的行为。</p>
      <p>第三条　在中华人民共和国境内设立的金融机构和按照规定应当履行反洗钱义务的特定非金融机构，应当依法采取预防、监控措施，建立健全客户身份识别制度、客户身份资料和交易记录保存制度、大额交易和可疑交易报告制度，履行反洗钱义务。</p>
      <p>第四条　国务院反洗钱行政主管部门负责全国的反洗钱监督管理工作。国务院有关部门、机构在各自的职责范围内履行反洗钱监督管理职责。     国务院反洗钱行政主管部门、国务院有关部门、机构和司法机关在反洗钱工作中应当相互配合。</p>
      <p>第五条　对依法履行反洗钱职责或者义务获得的客户身份资料和交易信息，应当予以保密；非依法律规定，不得向任何单位和个人提供。</p>
      <p>反洗钱行政主管部门和其他依法负有反洗钱监督管理职责的部门、机构履行反洗钱职责获得的客户身份资料和交易信息，只能用于反洗钱行政调查。</p>
      <p>司法机关依照本法获得的客户身份资料和交易信息，只能用于反洗钱刑事诉讼。</p>
      <p>第六条　履行反洗钱义务的机构及其工作人员依法提交大额交易和可疑交易报告，受法律保护。</p>
      <p>第七条　任何单位和个人发现洗钱活动，有权向反洗钱行政主管部门或者公安机关举报。接受举报的机关应当对举报人和举报内容保密。</p>
      <p class="text-center">第二章　反洗钱监督管理</p>
      <p>第八条　国务院反洗钱行政主管部门组织、协调全国的反洗钱工作，负责反洗钱的资金监测，制定或者会同国务院有关金融监督管理机构制定金融机构反洗钱规章，监督、检查金融机构履行反洗钱义务的情况，在职责范围内调查可疑交易活动，履行法律和国务院规定的有关反洗钱的其他职责。</p>
      <p>国务院反洗钱行政主管部门的派出机构在国务院反洗钱行政主管部门的授权范围内，对金融机构履行反洗钱义务的情况进行监督、检查。</p>
      <p>第九条　国务院有关金融监督管理机构参与制定所监督管理的金融机构反洗钱规章，对所监督管理的金融机构提出按照规定建立健全反洗钱内部控制制度的要求，履行法律和国务院规定的有关反洗钱的其他职责。</p>
      <p>第十条　国务院反洗钱行政主管部门设立反洗钱信息中心，负责大额交易和可疑交易报告的接收、分析，并按照规定向国务院反洗钱行政主管部门报告分析结果，履行国务院反洗钱行政主管部门规定的其他职责。</p>
      <p>第十一条　国务院反洗钱行政主管部门为履行反洗钱资金监测职责，可以从国务院有关部门、机构获取所必需的信息，国务院有关部门、机构应当提供。</p>
      <p>国务院反洗钱行政主管部门应当向国务院有关部门、机构定期通报反洗钱工作情况。</p>
      <p>第十二条　海关发现个人出入境携带的现金、无记名有价证券超过规定金额的，应当及时向反洗钱行政主管部门通报。</p>
      <p>前款应当通报的金额标准由国务院反洗钱行政主管部门会同海关总署规定。</p>
      <p>第十三条　反洗钱行政主管部门和其他依法负有反洗钱监督管理职责的部门、机构发现涉嫌洗钱犯罪的交易活动，应当及时向侦查机关报告。</p>
      <p>第十四条　国务院有关金融监督管理机构审批新设金融机构或者金融机构增设分支机构时，应当审查新机构反洗钱内部控制制度的方案；对于不符合本法规定的设立申请，不予批准。</p>
      <p class="text-center">第三章　金融机构反洗钱义务</p>
      <p>第十五条　金融机构应当依照本法规定建立健全反洗钱内部控制制度，金融机构的负责人应当对反洗钱内部控制制度的有效实施负责。</p>
      <p>金融机构应当设立反洗钱专门机构或者指定内设机构负责反洗钱工作。</p>
      <p>第十六条　金融机构应当按照规定建立客户身份识别制度。</p>
      <p>金融机构在与客户建立业务关系或者为客户提供规定金额以上的现金汇款、现钞兑换、票据兑付等一次性金融服务时，应当要求客户出示真实有效的身份证件或者其他身份证明文件，进行核对并登记。</p>
      <p>客户由他人代理办理业务的，金融机构应当同时对代理人和被代理人的身份证件或者其他身份证明文件进行核对并登记。</p>
      <p>与客户建立人身保险、信托等业务关系，合同的受益人不是客户本人的，金融机构还应当对受益人的身份证件或者其他身份证明文件进行核对并登记。</p>
      <p>金融机构不得为身份不明的客户提供服务或者与其进行交易，不得为客户开立匿名账户或者假名账户。</p>
      <p>金融机构对先前获得的客户身份资料的真实性、有效性或者完整性有疑问的，应当重新识别客户身份。</p>
      <p>任何单位和个人在与金融机构建立业务关系或者要求金融机构为其提供一次性金融服务时，都应当提供真实有效的身份证件或者其他身份证明文件。</p>
      <p>第十七条　金融机构通过第三方识别客户身份的，应当确保第三方已经采取符合本法要求的客户身份识别措施；第三方未采取符合本法要求的客户身份识别措施的，由该金融机构承担未履行客户身份识别义务的责任。</p>
      <p>第十八条　金融机构进行客户身份识别，认为必要时，可以向公安、工商行政管理等部门核实客户的有关身份信息。</p>
      <p>第十九条　金融机构应当按照规定建立客户身份资料和交易记录保存制度。</p>
      <p>在业务关系存续期间，客户身份资料发生变更的，应当及时更新客户身份资料。</p>
      <p>客户身份资料在业务关系结束后、客户交易信息在交易结束后，应当至少保存五年。</p>
      <p>金融机构破产和解散时，应当将客户身份资料和客户交易信息移交国务院有关部门指定的机构。</p>
      <p>第二十条　金融机构应当按照规定执行大额交易和可疑交易报告制度。</p>
      <p>金融机构办理的单笔交易或者在规定期限内的累计交易超过规定金额或者发现可疑交易的，应当及时向反洗钱信息中心报告。</p>
      <p>第二十一条　金融机构建立客户身份识别制度、客户身份资料和交易记录保存制度的具体办法，由国务院反洗钱行政主管部门会同国务院有关金融监督管理机构制定。金融机构大额交易和可疑交易报告的具体办法，由国务院反洗钱行政主管部门制定。</p>
      <p>第二十二条　金融机构应当按照反洗钱预防、监控制度的要求，开展反洗钱培训和宣传工作。</p>
      <p class="text-center">第四章　反洗钱调查</p>
      <p>第二十三条　国务院反洗钱行政主管部门或者其省一级派出机构发现可疑交易活动，需要调查核实的，可以向金融机构进行调查，金融机构应当予以配合，如实提供有关文件和资料。</p>
      <p>第二十四条　调查可疑交易活动，可以询问金融机构有关人员，要求其说明情况。</p>
      <p>询问应当制作询问笔录。询问笔录应当交被询问人核对。记载有遗漏或者差错的，被询问人可以要求补充或者更正。被询问人确认笔录无误后，应当签名或者盖章；调查人员也应当在笔录上签名。</p>
      <p>第二十五条　调查中需要进一步核查的，经国务院反洗钱行政主管部门或者其省一级派出机构的负责人批准，可以查阅、复制被调查对象的账户信息、交易记录和其他有关资料；对可能被转移、隐藏、篡改或者毁损的文件、资料，可以予以封存。</p>
      <p>调查人员封存文件、资料，应当会同在场的金融机构工作人员查点清楚，当场开列清单一式二份，由调查人员和在场的金融机构工作人员签名或者盖章，一份交金融机构，一份附卷备查。</p>
      <p>第二十六条　经调查仍不能排除洗钱嫌疑的，应当立即向有管辖权的侦查机关报案。客户要求将调查所涉及的账户资金转往境外的，经国务院反洗钱行政主管部门负责人批准，可以采取临时冻结措施。</p>
      <p>侦查机关接到报案后，对已依照前款规定临时冻结的资金，应当及时决定是否继续冻结。侦查机关认为需要继续冻结的，依照刑事诉讼法的规定采取冻结措施；认为不需要继续冻结的，应当立即通知国务院反洗钱行政主管部门，国务院反洗钱行政主管部门应当立即通知金融机构解除冻结。</p>
      <p>临时冻结不得超过四十八小时。金融机构在按照国务院反洗钱行政主管部门的要求采取临时冻结措施后四十八小时内，未接到侦查机关继续冻结通知的，应当立即解除冻结。</p>
      <p class="text-center">第五章　反洗钱国际合作</p>
      <p>第二十七条　中华人民共和国根据缔结或者参加的国际条约，或者按照平等互惠原则，开展反洗钱国际合作。</p>
      <p>第二十八条　国务院反洗钱行政主管部门根据国务院授权，代表中国政府与外国政府和有关国际组织开展反洗钱合作，依法与境外反洗钱机构交换与反洗钱有关的信息和资料。</p>
      <p>第二十九条　涉及追究洗钱犯罪的司法协助，由司法机关依照有关法律的规定办理。</p>
      <p class="text-center">第六章　法律责任</p>
      <p>第三十条　反洗钱行政主管部门和其他依法负有反洗钱监督管理职责的部门、机构从事反洗钱工作的人员有下列行为之一的，依法给予行政处分：</p>
      <p>（一）违反规定进行检查、调查或者采取临时冻结措施的；</p>
      <p>（二）泄露因反洗钱知悉的国家秘密、商业秘密或者个人隐私的；</p>
      <p>（三）违反规定对有关机构和人员实施行政处罚的；</p>
      <p>（四）其他不依法履行职责的行为。</p>
      <p>第三十一条　金融机构有下列行为之一的，由国务院反洗钱行政主管部门或者其授权的设区的市一级以上派出机构责令限期改正；情节严重的，建议有关金融监督管理机构依法责令金融机构对直接负责的董事、高级管理人员和其他直接责任人员给予纪律处分：</p>
      <p>（一）未按照规定建立反洗钱内部控制制度的；</p>
      <p>（二）未按照规定设立反洗钱专门机构或者指定内设机构负责反洗钱工作的；</p>
      <p>（三）未按照规定对职工进行反洗钱培训的。</p>
      <p>第三十二条　金融机构有下列行为之一的，由国务院反洗钱行政主管部门或者其授权的设区的市一级以上派出机构责令限期改正；情节严重的，处二十万元以上五十万元以下罚款，并对直接负责的董事、高级管理人员和其他直接责任人员，处一万元以上五万元以下罚款：</p>
      <p>（一）未按照规定履行客户身份识别义务的；</p>
      <p>（二）未按照规定保存客户身份资料和交易记录的；</p>
      <p>（三）未按照规定报送大额交易报告或者可疑交易报告的；</p>
      <p>（四）与身份不明的客户进行交易或者为客户开立匿名账户、假名账户的；</p>
      <p>（五）违反保密规定，泄露有关信息的；</p>
      <p>（六）拒绝、阻碍反洗钱检查、调查的；</p>
      <p>（七）拒绝提供调查材料或者故意提供虚假材料的。</p>
      <p>金融机构有前款行为，致使洗钱后果发生的，处五十万元以上五百万元以下罚款，并对直接负责的董事、高级管理人员和其他直接责任人员处五万元以上五十万元以下罚款；情节特别严重的，反洗钱行政主管部门可以建议有关金融监督管理机构责令停业整顿或者吊销其经营许可证。</p>
      <p>对有前两款规定情形的金融机构直接负责的董事、高级管理人员和其他直接责任人员，反洗钱行政主管部门可以建议有关金融监督管理机构依法责令金融机构给予纪律处分，或者建议依法取消其任职资格、禁止其从事有关金融行业工作。</p>
      <p>第三十三条　违反本法规定，构成犯罪的，依法追究刑事责任。</p>
      <p class="text-center">第七章　附　则</p>
      <p>第三十四条　本法所称金融机构，是指依法设立的从事金融业务的政策性银行、商业银行、信用合作社、邮政储汇机构、信托投资公司、证券公司、期货经纪公司、保险公司以及国务院反洗钱行政主管部门确定并公布的从事金融业务的其他机构。</p>
      <p>第三十五条　应当履行反洗钱义务的特定非金融机构的范围、其履行反洗钱义务和对其监督管理的具体办法，由国务院反洗钱行政主管部门会同国务院有关部门制定。</p>
      <p>第三十六条　对涉嫌恐怖活动资金的监控适用本法；其他法律另有规定的，适用其规定。</p>
      <p>第三十七条　本法自2007年1月1日起施行。</p>
