      <p>为鼓励金融创新，促进互联网金融健康发展，明确监管责任，规范市场秩序，经党中央、国务院同意，中国人民银行、工业和信息化部、公安部、财政部、国家工商总局、国务院法制办、中国银行业监督管理委员会、中国证券监督管理委员会、中国保险监督管理委员会、国家互联网信息办公室日前联合印发了《关于促进互联网金融健康发展的指导意见》（银发〔2015〕221号，以下简称《指导意见》）。</p>
      <p>《指导意见》按照“鼓励创新、防范风险、趋利避害、健康发展”的总体要求，提出了一系列鼓励创新、支持互联网金融稳步发展的政策措施，积极鼓励互联网金融平台、产品和服务创新，鼓励从业机构相互合作，拓宽从业机构融资渠道，坚持简政放权和落实、完善财税政策，推动信用基础设施建设和配套服务体系建设。</p>
      <p>《指导意见》按照“依法监管、适度监管、分类监管、协同监管、创新监管”的原则，确立了互联网支付、网络借贷、股权众筹融资、互联网基金销售、互联网保险、互联网信托和互联网消费金融等互联网金融主要业态的监管职责分工，落实了监管责任，明确了业务边界。</p>
      <p>《指导意见》坚持以市场为导向发展互联网金融，遵循服务好实体经济、服从宏观调控和维护金融稳定的总体目标，切实保障消费者合法权益，维护公平竞争的市场秩序，在互联网行业管理，客户资金第三方存管制度，信息披露、风险提示和合格投资者制度，消费者权益保护，网络与信息安全，反洗钱和防范金融犯罪，加强互联网金融行业自律以及监管协调与数据统计监测等方面提出了具体要求。</p>
      <p>下一步，各相关部门将按照《指导意见》的职责分工，认真贯彻落实《指导意见》的各项要求；互联网金融行业从业机构应按照《指导意见》的相关规定，依法合规开展各项经营活动。（完）</p>
