      <p>1.5网络借贷信息中介机构以下简称“网贷机构”。</p>
      <p>2.1网贷机构备案信息</p>
      <p>2.1.1备案信息：指网贷机构已经备案登记的相关信息，包括备案登记地方金融监管部门、备案登记时间、备案登记编号（如有）等。</p>
      <p>2.1.2电信业务经营许可信息：指网贷机构获得的网络借贷中介业务电信业务经营许可证号。</p>
      <p>2.1.3资金存管信息：指网贷机构资金存管的银行全称。</p>
      <p>2.1.4网站备案图标及编号：指网贷机构获得的公安机关出具的网站备案图标及编号。</p>
      <p>2.1.5风险管理信息：指网贷机构风险管理架构、风险评估流程、风险预警管理情况、催收方式等信息。</p>
      <p>2.2网贷机构组织信息</p>
      <p>2.2.1网贷机构工商信息</p>
      <p>（1） 公司全称：指网贷机构在工商部门登记注册的公司全称。</p>
      <p>（2） 公司简称(常用名)：指网贷机构对外简称或常用简称，如有多个简称，应当逐一列明并以分号分隔。</p>
      <p>（3） 统一社会信用代码：指网贷机构在工商部门登记注册后获得的统一社会信用代码；若无统一社会信用代码，则填写组织机构代码。</p>
      <p>（4） 公司注册资本：指网贷机构在工商部门依法登记的注册资本。有限责任公司的注册资本为在工商部门依法登记的全体股东认缴的出资额。股份有限公司采取发起设立方式设立的，注册资本为在工商部门依法登记的全体发起人认购的股本总额；股份有限公司采取募集设立方式设立的，注册资本为在工商部门依法登记的实收股本总额。</p>
      <p>（5） 实缴注册资本：指网贷机构已实际出资的资金总额。</p>
      <p>（6） 公司注册地：指网贷机构在工商部门登记注册的公司地址。</p>
      <p>（7） 公司经营地：指网贷机构实际开展经营的地址，如有多个经营地，应当逐一列明并以分号分隔。</p>
      <p>（8） 公司成立时间：指网贷机构注册成立的日期，即营业执照上的公司成立日期。</p>
      <p>（9） 公司经营期限：指网贷机构在工商部门注册的存续期间。</p>
      <p>（10） 公司经营状态：指网贷机构目前公司经营状况，分为开业、停业、注销、吊销。若为停业状况，应补充说明原因。</p>
      <p>（11） 公司法定代表人：指网贷机构营业执照上登记的法定代表人姓名。</p>
      <p>（12） 公司经营范围：指网贷机构于工商登记注册部门核准登记的经营范围。</p>
      <p>2.2.2网贷机构股东信息</p>
      <p>（1） 公司股东名称：指网贷机构股东在工商部门依法登记注册的全称。</p>
      <p>（2） 公司股东占股比例：指网贷机构股东持有股份占网贷机构全部股份的比例，单位为百分比。</p>
      <p>2.2.3组织架构及从业人员概况</p>
      <p>（1） 组织架构：指网贷机构内部部门设置及层级。</p>
      <p>（2） 从业人员概况：指在网贷机构工作，由网贷机构支付工资的各类人员，以及有工作岗位，但由于学习、病休产假等原因暂未工作，仍由单位支付工资的员工，包括正式人员、劳务派遣人员、临时聘用人员等的人员总数、年龄分布、学历分布等情况。</p>
      <p>2.2.4分支机构信息</p>
      <p>（1） 分支机构全称：指网贷机构的分支机构在工商部门登记注册的公司全称。</p>
      <p>（2） 分支机构所在地：指网贷机构的分支机构在工商部门登记注册的公司地址。</p>
      <p>（3） 分支机构成立时间：指网贷机构的分支机构注册成立的日期，即分支机构营业执照上的分支机构成立日期。</p>
      <p>（4） 分支机构负责人：指网贷机构的分支机构的负责人姓名。</p>
      <p>（5） 分支机构联系电话：指网贷机构的分支机构的联系电话。</p>
      <p>（6） 分支机构投诉电话：指网贷机构的分支机构的投诉电话。</p>
      <p>（7） 分支机构员工人数：指网贷机构的分支机构的员工总人数。同时应当区分正式员工、派遣员工、临时员工数量。</p>
      <p>2.2.5渠道信息</p>
      <p>（1） 公司官方网址：指网贷机构在运营的网站域名及IP地址。</p>
      <p>（2） 平台APP名称、微信公众号、微博：指网贷机构依法注册并使用的开展网络借贷信息中介服务的APP、社交媒体账号及IP地址（或链接）。</p>
      <p>2.3网贷机构审核信息</p>
      <p>2.3.1财务审计报告：指会计师事务所出具的网贷机构上一年度审计报告。</p>
      <p>2.3.2重点环节审计结果：指会计师事务所出具的对网贷机构出借人与借款人资金存管、信息披露情况、信息科技基础设施安全、经营合规性、资金运用流程等重点环节的审计结果。</p>
      <p>2.3.3合规报告：指律师事务所出具的对网贷机构合规情况审查报告。</p>
      <p>2.4网贷机构经营信息</p>
      <p>2.4.1累计交易总额：指自网贷机构成立起，经网贷机构撮合完成的借款项目的本金总合。</p>
      <p>2.4.2累计交易笔数：指自网贷机构成立起，经网贷机构撮合完成的借款交易笔数总合。</p>
      <p>2.4.3借贷余额：指截至统计时点，通过网贷机构已经上线运行的网络借贷信息中介平台完成的借款总余额。</p>
      <p>2.4.4累计借款人数量：指借款人通过网贷机构成功借款的借款人总数。同一借款人多次借款的，按实际借款人计算。（例如：张三借款3次，累计借款人数量为1）。</p>
      <p>2.4.5累计出借人数量：指出借人通过网贷机构成功出借资金的出借人总数。同一出借人多次出借的，按实际出借人计算。（例如：张三出借3次，累计出借人数量为1）。</p>
      <p>2.4.6当前借款人数量：指截至统计时点仍存在待还借款的借款人总数。同一借款人多次借款的，按实际借款人计算。</p>
      <p>2.4.7当前出借人数量：指截至统计时点仍存在待收借款的出借人总数。同一出借人多次出借的，按实际出借人计算。</p>
      <p>2.4.8前十大借款人待还金额占比：指在平台撮合的项目中，借款最多的前十户借款人的借款余额占总借款余额的比例。</p>
      <p>2.4.9最大单一借款人待还金额占比：指在平台撮合的项目中，借款最多一户借款人的借款余额占总借款余额的比例。</p>
      <p>2.4.10关联关系借款余额：指截至统计时点，与平台具有关联关系的借款人通过平台撮合完成的借款总余额。关联关系指网络借贷信息中介机构主要股东、实际控制人、董事、监事、高级管理人员与其直接或间接控制、有重大影响的企业、自然人之间的关系，以及可能导致网络借贷信息中介机构利益转移的其他关系（主要股东，指持有或控制网络信息借贷中介机构5%以上股份或表决权的自然人、法人或其他组织；直接或间接控制企业，指直接或间接持有企业5%以上股份或表决权）。</p>
      <p>2.4.11逾期金额：指按合同约定，出借人到期未收到本金和利息的金额总合。收到，是指资金实际划付至出借人银行账户。</p>
      <p>2.4.12逾期笔数：指按合同约定，出借人到期未收到本金和利息的借款的笔数。收到，是指资金实际划付至出借人银行账户。</p>
      <p>2.4.13逾期90天以上金额：指逾期90天（不含）以上的借款本金余额。</p>
      <p>2.4.14逾期90天以上笔数：指逾期90天（不含）以上的借款的笔数。</p>
      <p>2.4.15代偿金额：指因借款方违约等原因第三方（非借款人、非网贷机构）代为偿还的总金额。</p>
      <p>2.4.16代偿笔数：指因借款方违约等原因第三方（非借款人、非网贷机构）代为偿还的笔数。</p>
      <p>2.4.17收费标准：指网贷机构向借款人收取费用的名目及费用计算标准。如涉及多个收费项目，应当逐一列明。</p>
      <p>2.5网贷机构项目信息</p>
      <p>2.5.1借款人基本信息</p>
      <p>（1） 借款人主体性质：指借款人为自然人、法人或其他组织。</p>
      <p>（2） 借款人所属行业：指借款自然人所在单位、借款法人或其他组织根据《国民经济行业分类》划分的行业类别。</p>
      <p>（3） 借款人收入及负债情况：指借款人在日常活动中所形成的、会导致所有者权益增加的、非所有者投入资本的经济利益的总流入，以及借款人过去的交易或者事项形成的、预期会导致经济利益流出企业的现时义务。</p>
      <p>（4） 借款人征信报告情况：指脱敏处理后，经借款人授权由中国人民银行征信系统出具的征信报告中借款人的逾期情况。</p>
      <p>2.5.2项目基本信息</p>
      <p>（5） 项目名称和简介：指网络借贷信息中介平台上展示的借款人借款项目的名称和基本情况介绍。</p>
      <p>（6） 借款金额：指借款人申请借款的本金金额。</p>
      <p>（7） 借款期限：指借款人申请借款的时长，应当以天、月、年为单位列明。</p>
      <p>（8） 借款用途：指借款人申请借款的具体去向。</p>
      <p>（9） 还款方式：还款方式应当以文字说明，并向出借人列明计算方式。如：按月付息到期还本。借款金额为X，年利率为Y，借款期限为Z月，则每月应还利息计算公式为：X×Y/12,应还总利息计算公式为：X×Y/12×Z。应还本金为X。</p>
      <p>（10） 年化利率：指借款人向出借人支付的利息费率，利率应当以年化形式披露，年以365天计算。</p>
      <p>（11） 起息日：指利息产生的起始日期。</p>
      <p>（12） 还款来源：指借款人借款的还款依据。</p>
      <p>（13）  担保措施：指在借款活动中，债权人为保障其债权的实现，要求债务人向债权人提供担保的方式（包括担保主体名称、担保措施、是否已履行完毕法律法规需办理的相关手续等信息）。</p>
