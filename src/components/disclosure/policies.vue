      <p>（２００４年８月２８日第十届全国人民代表大会常务委员会第十一次会议通过）</p>
      <p class="text-center">目  录</p>
      <p>第一章 总则</p>
      <p>第二章 数据电文</p>
      <p>第三章 电子签名与认证</p>
      <p>第四章 法律责任</p>
      <p>第五章 附则</p>
      <p class="text-center">第一章 总则</p>
      <p>第一条 为了规范电子签名行为，确立电子签名的法律效力，维护有关各方的合法权益，制定本法。</p>
      <p>第二条 本法所称电子签名，是指数据电文中以电子形式所含、所附用于识别签名人身份并表明签名人认可其中内容的数据。</p>
      <p>本法所称数据电文，是指以电子、光学、磁或者类似手段生成、发送、接收或者储存的信息。</p>
      <p>第三条 民事活动中的合同或者其他文件、单证等文书，当事人可以约定使用或者不使用电子签名、数据电文。</p>
      <p>当事人约定使用电子签名、数据电文的文书，不得仅因为其采用电子签名、数据电文的形式而否定其法律效力。</p>
      <p>前款规定不适用下列文书：</p>
      <p>（一）涉及婚姻、收养、继承等人身关系的；</p>
      <p>（二）涉及土地、房屋等不动产权益转让的；</p>
      <p>（三）涉及停止供水、供热、供气、供电等公用事业服务的；</p>
      <p>（四）法律、行政法规规定的不适用电子文书的其他情形。</p>
      <p>第二章 数据电文</p>
      <p>第四条 能够有形地表现所载内容，并可以随时调取查用的数据电文，视为符合法律、法规要求的书面形式。</p>
      <p>第五条 符合下列条件的数据电文，视为满足法律、法规规定的原件形式要求：</p>
      <p>（一）能够有效地表现所载内容并可供随时调取查用；</p>
      <p>（二）能够可靠地保证自最终形成时起，内容保持完整、未被更改。但是，在数据电文上增加背书以及数据交换、储存和显示过程中发生的形式变化不影响数据电文的完整性。</p>
      <p>第六条 符合下列条件的数据电文，视为满足法律、法规规定的文件保存要求：</p>
      <p>（一）能够有效地表现所载内容并可供随时调取查用；</p>
      <p>（二）数据电文的格式与其生成、发送或者接收时的格式相同，或者格式不相同但是能够准确表现原来生成、发送或者接收的内容；</p>
      <p>（三）能够识别数据电文的发件人、收件人以及发送、接收的时间。</p>
      <p>第七条 数据电文不得仅因为其是以电子、光学、磁或者类似手段生成、发送、接收或者储存的而被拒绝作为证据使用。</p>
      <p>第八条 审查数据电文作为证据的真实性，应当考虑以下因素：</p>
      <p>（一）生成、储存或者传递数据电文方法的可靠性；</p>
      <p>（二）保持内容完整性方法的可靠性；</p>
      <p>（三）用以鉴别发件人方法的可靠性；</p>
      <p>（四）其他相关因素。</p>
      <p>第九条数据电文有下列情形之一的，视为发件人发送：</p>
      <p>（一）经发件人授权发送的；</p>
      <p>（二）发件人的信息系统自动发送的；</p>
      <p>（三）收件人按照发件人认可的方法对数据电文进行验证后结果相符的。当事人对前款规定的事项另有约定的，从其约定。</p>
      <p>第十条 法律、行政法规规定或者当事人约定数据电文需要确认收讫的，应当确认收讫。发件人收到收件人的收讫确认时，数据电文视为已经收到。</p>
      <p>第十一 条数据电文进入发件人控制之外的某个信息系统的时间，视为该数据电文的发送时间。</p>
      <p>收件人指定特定系统接收数据电文的，数据电文进入该特定系统的时间，视为该数据电文的接收时间；未指定特定系统的，数据电文进入收件人的任何系统的首次时间，视为该数据电文的接收时间。</p>
      <p>第十二条发件人的主营业地为数据电文的发送地点，收件人的主营业地为数据电文的接收地点。没有主营业地的，其经常居住地为发送或者接收地点。</p>
      <p class="text-center">第三章 电子签名与认证</p>
      <p>第十三条电子签名同时符合下列条件的，视为可靠的电子签名：</p>
      <p>（一）电子签名制作数据用于电子签名时，属于电子签名人专有；</p>
      <p>（二）签署时电子签名制作数据仅由电子签名人控制；</p>
      <p>（三）签署后对电子签名的任何改动能够被发现；</p>
      <p>（四）签署后对数据电文内容和形式的任何改动能够被发现。当事人也可以选择使用符合其约定的可靠条件的电子签名。</p>
      <p>第十四条 可靠的电子签名与手写签名或者盖章具有同等的法律效力。</p>
      <p>第十五条 电子签名人应当妥善保管电子签名制作数据。电子签名人知悉电子签名制作数据已经失密或者可能已经失密时，应当及时告知有关各方，并终止使用该电子签名制作数据。</p>
      <p>第十六条 电子签名需要第三方认证的，由依法设立的电子认证服务提供者提供认证服务。</p>
      <p>第十七 条提供电子认证服务，应当具备下列条件：</p>
      <p>（一）具有与提供电子认证服务相适应的专业技术人员和管理人员；</p>
      <p>（二）具有与提供电子认证服务相适应的资金和经营场所；</p>
      <p>（三）具有符合国家安全标准的技术和设备；</p>
      <p>（四）具有国家密码管理机构同意使用密码的证明文件；</p>
      <p>（五）法律、行政法规规定的其他条件。</p>
      <p>第十八条 从事电子认证服务，应当向国务院信息产业主管部门提出申请，并提交符合本法第十七条规定条件的相关材料。国务院信息产业主管部门接到申请后经依法审查，征求国务院商务主管部门等有关部门的意见后，自接到申请之日起四十五日内作出许可或者不予许可的决定。予以许可的，颁发电子认证许可证书；不予许可的，应当书面通知申请人并告知理由。</p>
      <p>申请人应当持电子认证许可证书依法向工商行政管理部门办理企业登记手续。</p>
      <p>取得认证资格的电子认证服务提供者，应当按照国务院信息产业主管部门的规定在互联网上公布其名称、许可证号等信息。</p>
      <p>第十九条电子认证服务提供者应当制定、公布符合国家有关规定的电子认证业务规则，并向国务院信息产业主管部门备案。</p>
      <p>电子认证业务规则应当包括责任范围、作业操作规范、信息安全保障措施等事项。</p>
      <p>第二十条电子签名人向电子认证服务提供者申请电子签名认证证书，应当提供真实、完整和准确的信息。</p>
      <p>电子认证服务提供者收到电子签名认证证书申请后，应当对申请人的身份进行查验，并对有关材料进行审查。</p>
      <p>第二十一条电子认证服务提供者签发的电子签名认证证书应当准确无误，并应当载明下列内容：</p>
      <p>（一）电子认证服务提供者名称；</p>
      <p>（二）证书持有人名称；</p>
      <p>（三）证书序列号；</p>
      <p>（四）证书有效期；</p>
      <p>（五）证书持有人的电子签名验证数据；</p>
      <p>（六）电子认证服务提供者的电子签名；</p>
      <p>（七）国务院信息产业主管部门规定的其他内容。</p>
      <p>第二十二条 电子认证服务提供者应当保证电子签名认证证书内容在有效期内完整、准确，并保证电子签名依赖方能够证实或者了解电子签名认证证书所载内容及其他有关事项。</p>
      <p>第二十三条 电子认证服务提供者拟暂停或者终止电子认证服务的，应当在暂停或者终止服务九十日前，就业务承接及其他有关事项通知有关各方。</p>
      <p>电子认证服务提供者拟暂停或者终止电子认证服务的，应当在暂停或者终止服务六十日前向国务院信息产业主管部门报告，并与其他电子认证服务提供者就业务承接进行协商，作出妥善安排。</p>
      <p>电子认证服务提供者未能就业务承接事项与其他电子认证服务提供者达成协议的，应当申请国务院信息产业主管部门安排其他电子认证服务提供者承接其业务。</p>
      <p>电子认证服务提供者被依法吊销电子认证许可证书的，其业务承接事项的处理按照国务院信息产业主管部门的规定执行。</p>
      <p>第二十四条 电子认证服务提供者应当妥善保存与认证相关的信息，信息保存期限至少为电子签名认证证书失效后五年。</p>
      <p>第二十五条 国务院信息产业主管部门依照本法制定电子认证服务业的具体管理办法，对电子认证服务提供者依法实施监督管理。</p>
      <p>第二十六条 经国务院信息产业主管部门根据有关协议或者对等原则核准后，中华人民共和国境外的电子认证服务提供者在境外签发的电子签名认证证书与依照本法设立的电子认证服务提供者签发的电子签名认证证书具有同等的法律效力。</p>
      <p class="text-center">第四章 法律责任</p>
      <p>第二十七条 电子签名人知悉电子签名制作数据已经失密或者可能已经失密未及时告知有关各方、并终止使用电子签名制作数据，未向电子认证服务提供者提供真实、完整和准确的信息，或者有其他过错，给电子签名依赖方、电子认证服务提供者造成损失的，承担赔偿责任。</p>
      <p>第二十八条 电子签名人或者电子签名依赖方因依据电子认证服务提供者提供的电子签名认证服务从事民事活动遭受损失，电子认证服务提供者不能证明自己无过错的，承担赔偿责任。</p>
      <p>第二十九条 未经许可提供电子认证服务的，由国务院信息产业主管部门责令停止违法行为；有违法所得的，没收违法所得；违法所得三十万元以上的，处违法所得一倍以上三倍以下的罚款；没有违法所得或者违法所得不足三十万元的，处十万元以上三十万元以下的罚款。</p>
      <p>第三十条 电子认证服务提供者暂停或者终止电子认证服务，未在暂停或者终止服务六十日前向国务院信息产业主管部门报告的，由国务院信息产业主管部门对其直接负责的主管人员处一万元以上五万元以下的罚款。</p>
      <p>第三十一条 电子认证服务提供者不遵守认证业务规则、未妥善保存与认证相关的信息，或者有其他违法行为的，由国务院信息产业主管部门责令限期改正；逾期未改正的，吊销电子认证许可证书，其直接负责的主管人员和其他直接责任人员十年内不得从事电子认证服务。吊销电子认证许可证书的，应当予以公告并通知工商行政管理部门。</p>
      <p>第三十二条 伪造、冒用、盗用他人的电子签名，构成犯罪的，依法追究刑事责任；给他人造成损失的，依法承担民事责任。</p>
      <p>第三十三条 依照本法负责电子认证服务业监督管理工作的部门的工作人员，不依法履行行政许可、监督管理职责的，依法给予行政处分；构成犯罪的，依法追究刑事责任。</p>
      <p class="text-center">第五章 附则</p>
      <p>第三十四条本法中下列用语的含义：</p>
      <p>（一）电子签名人，是指持有电子签名制作数据并以本人身份或者以其所代表的人的名义实施电子签名的人；</p>
      <p>（二）电子签名依赖方，是指基于对电子签名认证证书或者电子签名的信赖从事有关活动的人；</p>
      <p>（三）电子签名认证证书，是指可证实电子签名人与电子签名制作数据有联系的数据电文或者其他电子记录；</p>
      <p>（四）电子签名制作数据，是指在电子签名过程中使用的，将电子签名与电子签名人可靠地联系起来的字符、编码等数据；</p>
      <p>（五）电子签名验证数据，是指用于验证电子签名的数据，包括代码、口令、算法或者公钥等。</p>
      <p>第三十五条 国务院或者国务院规定的部门可以依据本法制定政务活动和其他社会活动中使用电子签名、数据电文的具体办法。</p>
      <p>第三十六条 本法自２００５年４月１日起施行。</p>
