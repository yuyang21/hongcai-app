      <p>近年来，互联网技术、信息通信技术不断取得突破，推动互联网与金融快速融合，促进了金融创新，提高了金融资源配置效率，但也存在一些问题和风险隐患。为全面贯彻落实党的十八大和十八届二中、三中、四中全会精神，按照党中央、国务院决策部署，遵循“鼓励创新、防范风险、趋利避害、健康发展”的总体要求，从金融业健康发展全局出发，进一步推进金融改革创新和对外开放，促进互联网金融健康发展，经党中央、国务院同意，现提出以下意见。</p>
      <p>一、鼓励创新，支持互联网金融稳步发展</p>
      <p>互联网金融是传统金融机构与互联网企业（以下统称从业机构）利用互联网技术和信息通信技术实现资金融通、支付、投资和信息中介服务的新型金融业务模式。互联网与金融深度融合是大势所趋，将对金融产品、业务、组织和服务等方面产生更加深刻的影响。互联网金融对促进小微企业发展和扩大就业发挥了现有金融机构难以替代的积极作用，为大众创业、万众创新打开了大门。促进互联网金融健康发展，有利于提升金融服务质量和效率，深化金融改革，促进金融创新发展，扩大金融业对内对外开放，构建多层次金融体系。作为新生事物，互联网金融既需要市场驱动，鼓励创新，也需要政策助力，促进发展。</p>
      <p>（一）积极鼓励互联网金融平台、产品和服务创新，激发市场活力。鼓励银行、证券、保险、基金、信托和消费金融等金融机构依托互联网技术，实现传统金融业务与服务转型升级，积极开发基于互联网技术的新产品和新服务。支持有条件的金融机构建设创新型互联网平台开展网络银行、网络证券、网络保险、网络基金销售和网络消费金融等业务。支持互联网企业依法合规设立互联网支付机构、网络借贷平台、股权众筹融资平台、网络金融产品销售平台，建立服务实体经济的多层次金融服务体系，更好地满足中小微企业和个人投融资需求，进一步拓展普惠金融的广度和深度。鼓励电子商务企业在符合金融法律法规规定的条件下自建和完善线上金融服务体系，有效拓展电商供应链业务。鼓励从业机构积极开展产品、服务、技术和管理创新，提升从业机构核心竞争力。</p>
      <p>（二）鼓励从业机构相互合作，实现优势互补。支持各类金融机构与互联网企业开展合作，建立良好的互联网金融生态环境和产业链。鼓励银行业金融机构开展业务创新，为第三方支付机构和网络贷款平台等提供资金存管、支付清算等配套服务。支持小微金融服务机构与互联网企业开展业务合作，实现商业模式创新。支持证券、基金、信托、消费金融、期货机构与互联网企业开展合作，拓宽金融产品销售渠道，创新财富管理模式。鼓励保险公司与互联网企业合作，提升互联网金融企业风险抵御能力。</p>
      <p>（三）拓宽从业机构融资渠道，改善融资环境。支持社会资本发起设立互联网金融产业投资基金，推动从业机构与创业投资机构、产业投资基金深度合作。鼓励符合条件的优质从业机构在主板、创业板等境内资本市场上市融资。鼓励银行业金融机构按照支持小微企业发展的各项金融政策，对处于初创期的从业机构予以支持。针对互联网企业特点，创新金融产品和服务。</p>
      <p>（四）坚持简政放权，提供优质服务。各金融监管部门要积极支持金融机构开展互联网金融业务。按照法律法规规定，对符合条件的互联网企业开展相关金融业务实施高效管理。工商行政管理部门要支持互联网企业依法办理工商注册登记。电信主管部门、国家互联网信息管理部门要积极支持互联网金融业务，电信主管部门对互联网金融业务涉及的电信业务进行监管，国家互联网信息管理部门负责对金融信息服务、互联网信息内容等业务进行监管。积极开展互联网金融领域立法研究，适时出台相关管理规章，营造有利于互联网金融发展的良好制度环境。加大对从业机构专利、商标等知识产权的保护力度。鼓励省级人民政府加大对互联网金融的政策支持。支持设立专业化互联网金融研究机构，鼓励建设互联网金融信息交流平台，积极开展互联网金融研究。</p>
      <p>（五）落实和完善有关财税政策。按照税收公平原则，对于业务规模较小、处于初创期的从业机构，符合我国现行对中小企业特别是小微企业税收政策条件的，可按规定享受税收优惠政策。结合金融业营业税改征增值税改革，统筹完善互联网金融税收政策。落实从业机构新技术、新产品研发费用税前加计扣除政策。</p>
      <p>（六）推动信用基础设施建设，培育互联网金融配套服务体系。支持大数据存储、网络与信息安全维护等技术领域基础设施建设。鼓励从业机构依法建立信用信息共享平台。推动符合条件的相关从业机构接入金融信用信息基础数据库。允许有条件的从业机构依法申请征信业务许可。支持具备资质的信用中介组织开展互联网企业信用评级，增强市场信息透明度。鼓励会计、审计、法律、咨询等中介服务机构为互联网企业提供相关专业服务。</p>
      <br>
      <p>二、分类指导，明确互联网金融监管责任</p>
      <p>互联网金融本质仍属于金融，没有改变金融风险隐蔽性、传染性、广泛性和突发性的特点。加强互联网金融监管，是促进互联网金融健康发展的内在要求。同时，互联网金融是新生事物和新兴业态，要制定适度宽松的监管政策，为互联网金融创新留有余地和空间。通过鼓励创新和加强监管相互支撑，促进互联网金融健康发展，更好地服务实体经济。互联网金融监管应遵循“依法监管、适度监管、分类监管、协同监管、创新监管”的原则，科学合理界定各业态的业务边界及准入条件，落实监管责任，明确风险底线，保护合法经营，坚决打击违法和违规行为。</p>
      <p>（七）互联网支付。互联网支付是指通过计算机、手机等设备，依托互联网发起支付指令、转移货币资金的服务。互联网支付应始终坚持服务电子商务发展和为社会提供小额、快捷、便民小微支付服务的宗旨。银行业金融机构和第三方支付机构从事互联网支付，应遵守现行法律法规和监管规定。第三方支付机构与其他机构开展合作的，应清晰界定各方的权利义务关系，建立有效的风险隔离机制和客户权益保障机制。要向客户充分披露服务信息，清晰地提示业务风险，不得夸大支付服务中介的性质和职能。互联网支付业务由人民银行负责监管。</p>
      <p>（八）网络借贷。网络借贷包括个体网络借贷（即P2P网络借贷）和网络小额贷款。个体网络借贷是指个体和个体之间通过互联网平台实现的直接借贷。在个体网络借贷平台上发生的直接借贷行为属于民间借贷范畴，受合同法、民法通则等法律法规以及最高人民法院相关司法解释规范。个体网络借贷要坚持平台功能，为投资方和融资方提供信息交互、撮合、资信评估等中介服务。个体网络借贷机构要明确信息中介性质，主要为借贷双方的直接借贷提供信息服务，不得提供增信服务，不得非法集资。网络小额贷款是指互联网企业通过其控制的小额贷款公司，利用互联网向客户提供的小额贷款。网络小额贷款应遵守现有小额贷款公司监管规定，发挥网络贷款优势，努力降低客户融资成本。网络借贷业务由银监会负责监管。</p>
      <p>（九）股权众筹融资。股权众筹融资主要是指通过互联网形式进行公开小额股权融资的活动。股权众筹融资必须通过股权众筹融资中介机构平台（互联网网站或其他类似的电子媒介）进行。股权众筹融资中介机构可以在符合法律法规规定前提下，对业务模式进行创新探索，发挥股权众筹融资作为多层次资本市场有机组成部分的作用，更好服务创新创业企业。股权众筹融资方应为小微企业，应通过股权众筹融资中介机构向投资人如实披露企业的商业模式、经营管理、财务、资金使用等关键信息，不得误导或欺诈投资者。投资者应当充分了解股权众筹融资活动风险，具备相应风险承受能力，进行小额投资。股权众筹融资业务由证监会负责监管。</p>
      <p>（十）互联网基金销售。基金销售机构与其他机构通过互联网合作销售基金等理财产品的，要切实履行风险披露义务，不得通过违规承诺收益方式吸引客户；基金管理人应当采取有效措施防范资产配置中的期限错配和流动性风险；基金销售机构及其合作机构通过其他活动为投资人提供收益的，应当对收益构成、先决条件、适用情形等进行全面、真实、准确表述和列示，不得与基金产品收益混同。第三方支付机构在开展基金互联网销售支付服务过程中，应当遵守人民银行、证监会关于客户备付金及基金销售结算资金的相关监管要求。第三方支付机构的客户备付金只能用于办理客户委托的支付业务，不得用于垫付基金和其他理财产品的资金赎回。互联网基金销售业务由证监会负责监管。</p>
      <p>（十一）互联网保险。保险公司开展互联网保险业务，应遵循安全性、保密性和稳定性原则，加强风险管理，完善内控系统，确保交易安全、信息安全和资金安全。专业互联网保险公司应当坚持服务互联网经济活动的基本定位，提供有针对性的保险服务。保险公司应建立对所属电子商务公司等非保险类子公司的管理制度，建立必要的防火墙。保险公司通过互联网销售保险产品，不得进行不实陈述、片面或夸大宣传过往业绩、违规承诺收益或者承担损失等误导性描述。互联网保险业务由保监会负责监管。</p>
      <p>(十二)互联网信托和互联网消费金融。信托公司、消费金融公司通过互联网开展业务的，要严格遵循监管规定，加强风险管理，确保交易合法合规，并保守客户信息。信托公司通过互联网进行产品销售及开展其他信托业务的，要遵守合格投资者等监管规定，审慎甄别客户身份和评估客户风险承受能力，不能将产品销售给与风险承受能力不相匹配的客户。信托公司与消费金融公司要制定完善产品文件签署制度，保证交易过程合法合规，安全规范。互联网信托业务、互联网消费金融业务由银监会负责监管。</p>
      <br>
      <p>三、健全制度，规范互联网金融市场秩序</p>
      <p>发展互联网金融要以市场为导向，遵循服务实体经济、服从宏观调控和维护金融稳定的总体目标，切实保障消费者合法权益，维护公平竞争的市场秩序。要细化管理制度，为互联网金融健康发展营造良好环境。</p>
      <p>（十三）互联网行业管理。任何组织和个人开设网站从事互联网金融业务的，除应按规定履行相关金融监管程序外，还应依法向电信主管部门履行网站备案手续，否则不得开展互联网金融业务。工业和信息化部负责对互联网金融业务涉及的电信业务进行监管，国家互联网信息办公室负责对金融信息服务、互联网信息内容等业务进行监管，两部门按职责制定相关监管细则。</p>
      <p>（十四）客户资金第三方存管制度。除另有规定外，从业机构应当选择符合条件的银行业金融机构作为资金存管机构，对客户资金进行管理和监督，实现客户资金与从业机构自身资金分账管理。客户资金存管账户应接受独立审计并向客户公开审计结果。人民银行会同金融监管部门按照职责分工实施监管，并制定相关监管细则。</p>
      <p>（十五）信息披露、风险提示和合格投资者制度。从业机构应当对客户进行充分的信息披露，及时向投资者公布其经营活动和财务状况的相关信息，以便投资者充分了解从业机构运作状况，促使从业机构稳健经营和控制风险。从业机构应当向各参与方详细说明交易模式、参与方的权利和义务，并进行充分的风险提示。要研究建立互联网金融的合格投资者制度，提升投资者保护水平。有关部门按照职责分工负责监管。</p>
      <p>（十六）消费者权益保护。研究制定互联网金融消费者教育规划，及时发布维权提示。加强互联网金融产品合同内容、免责条款规定等与消费者利益相关的信息披露工作，依法监督处理经营者利用合同格式条款侵害消费者合法权益的违法、违规行为。构建在线争议解决、现场接待受理、监管部门受理投诉、第三方调解以及仲裁、诉讼等多元化纠纷解决机制。细化完善互联网金融个人信息保护的原则、标准和操作流程。严禁网络销售金融产品过程中的不实宣传、强制捆绑销售。人民银行、银监会、证监会、保监会会同有关行政执法部门，根据职责分工依法开展互联网金融领域消费者和投资者权益保护工作。</p>
      <p>（十七）网络与信息安全。从业机构应当切实提升技术安全水平，妥善保管客户资料和交易信息，不得非法买卖、泄露客户个人信息。人民银行、银监会、证监会、保监会、工业和信息化部、公安部、国家互联网信息办公室分别负责对相关从业机构的网络与信息安全保障进行监管，并制定相关监管细则和技术安全标准。</p>
      <p>（十八）反洗钱和防范金融犯罪。从业机构应当采取有效措施识别客户身份，主动监测并报告可疑交易，妥善保存客户资料和交易记录。从业机构有义务按照有关规定，建立健全有关协助查询、冻结的规章制度，协助公安机关和司法机关依法、及时查询、冻结涉案财产，配合公安机关和司法机关做好取证和执行工作。坚决打击涉及非法集资等互联网金融犯罪，防范金融风险，维护金融秩序。金融机构在和互联网企业开展合作、代理时应根据有关法律和规定签订包括反洗钱和防范金融犯罪要求的合作、代理协议，并确保不因合作、代理关系而降低反洗钱和金融犯罪执行标准。人民银行牵头负责对从业机构履行反洗钱义务进行监管，并制定相关监管细则。打击互联网金融犯罪工作由公安部牵头负责。</p>
      <p>（十九）加强互联网金融行业自律。充分发挥行业自律机制在规范从业机构市场行为和保护行业合法权益等方面的积极作用。人民银行会同有关部门，组建中国互联网金融协会。协会要按业务类型，制订经营管理规则和行业标准，推动机构之间的业务交流和信息共享。协会要明确自律惩戒机制，提高行业规则和标准的约束力。强化守法、诚信、自律意识，树立从业机构服务经济社会发展的正面形象，营造诚信规范发展的良好氛围。</p>
      <p>（二十）监管协调与数据统计监测。各监管部门要相互协作、形成合力，充分发挥金融监管协调部际联席会议制度的作用。人民银行、银监会、证监会、保监会应当密切关注互联网金融业务发展及相关风险，对监管政策进行跟踪评估，适时提出调整建议，不断总结监管经验。财政部负责互联网金融从业机构财务监管政策。人民银行会同有关部门，负责建立和完善互联网金融数据统计监测体系，相关部门按照监管职责分工负责相关互联网金融数据统计和监测工作，并实现统计数据和信息共享。</p>
    </div>
    <div v-if="id == 6">
      <p>P2P网络借贷（以下简称网贷）作为一种互联网金融业态，在缓解小微企业融资难、满足民间资本投资需求等方面发挥了积极作用。但近年来，网贷行业风险有所积聚，爆发了一系列风险事件，严重损害了广大投资者合法权益，对互联网金融行业声誉和健康发展造成较大负面影响，给金融安全和社会稳定带来较大危害。为贯彻落实党中央、国务院决策部署，促进网贷行业规范有序发展，根据《关于促进互联网金融健康发展的指导意见》（银发〔2015〕221号，以下简称《指导意见》）和《互联网金融风险专项整治工作实施方案》，制定本方案。</p>
      <p>一、工作目标和原则</p>
      <p>（一）工作目标</p>
      <p>按照任务要明、措施要实、责任要清、效果要好的要求，坚持重点整治与源头治理相结合、防范风险与创新发展相结合、清理整顿与依法打击相结合，妥善处置风险事件，遏制网贷领域风险事件高发势头，维护经济金融秩序和社会稳定。一是在市场主体层面，着力扶优抑劣，支持鼓励依法合规的网贷机构开展业务，促其健康发展，整治和取缔违法违规的网贷机构。二是在市场环境层面，加强规范优化，扭转行业机构异化趋势，实现正本清源，强化风险教育，引导出资人理性出资。三是在机制层面，坚持标本兼治，建立行业长效规范机制，消除监管空白，实现规范创新兼顾发展，形成良性循环。</p>
      <p>（二）工作原则</p>
      <p>态度积极，措施稳妥。高度重视本次专项整治工作，树立大局意识、责任意识，明确职责分工，确立时间进度表，积极推进各项工作。同时稳扎稳打，讲究方式方法，处理好工作力度和节奏的关系。</p>
      <p>底线思维，预案完备。充分认识网贷领域风险的复杂性、隐蔽性、突发性、涉众性、传染性，在统筹考虑各种突发风险的前提下，制定完备的处置预案，有序化解存量风险，有效控制增量风险，坚决守住不发生系统性区域性金融风险的底线。</p>
      <p>线上线下，统筹治理。兼顾市场主体的线上业务与线下实体，明确关联关系，依据其经营本质和实际控制人进行统筹治理。将从事线下金融业务活动的网贷机构及涉及网贷业务的综合性互联网金融平台纳入专项整治范围，做到风险防范和治理全覆盖。</p>
      <p>分类处理，标本兼治。根据网贷机构违法违规性质、情节和程度分类处理，精准施策，把专项整治工作与贯彻落实行业有关制度、促进网贷机构改革创新与重组改造结合起来，以本次专项整治工作为契机，强化行业监管，构建长效机制。</p>
      <p>依法合规，有章可循。贯彻落实《指导意见》、《互联网金融风险专项整治工作实施方案》和本方案明确的原则和要求，严格遵循有关法律法规和规章制度，做到依法整治、合规处理，为网贷行业常态化监管奠定基础。</p>
      <p>上下联动，协调配合。各有关部门、各地方人民政府加强组织领导，完善工作机制，充分考虑网贷行业跨区域、跨领域、跨行业的特点，加强部门间和区域间的协同联动，形成工作合力，提高整治效率，夯实整治基础，巩固整治成果。</p>
      <p>二、全面排查、摸清底数</p>
      <p>（一）排查目的</p>
      <p>准确掌握网贷机构相关数据，提高数据的权威性、准确性和及时性，摸清行业底数，建立较为完整的行业基本数据统计体系，为专项整治工作及今后的行业监管奠定坚实基础。</p>
      <p>（二）排查对象</p>
      <p>本次排查摸底的对象是各地经工商登记注册的网贷机构，根据《指导意见》要求，该类机构应当以互联网为主要渠道，为借款人与出借人（即贷款人）实现直接借贷提供信息搜集、信息公布、资信评估、信息交互、借贷撮合等服务。同时，部分以网贷名义开展经营，涉及资金归集、期限错配等行为，已经脱离信息中介本质，异化为信用中介的机构，也是本次排查和整治的对象。</p>
      <p>此外，对于互联网企业与银行业金融机构合作开展业务情况进行排查。互联网企业与银行业金融机构合作开展业务不得违反相关法律法规规定，不得通过互联网跨界开展金融活动进行监管套利。</p>
      <p>（三）排查方式</p>
      <p>采取多方数据汇总、逐一比对、网上核验、现场实地认证等方式进行。在数据汇总层面，银监会会同工业和信息化部、公安部、工商总局、国家互联网信息办公室及第三方统计机构、行业自律组织等，利用行业信息库、大数据检索、工商注册信息、接受举报等方式，汇总形成网贷机构基本数据统计，并发送至各省级人民政府。各省级人民政府以此为基础，综合采取公告确认、电话联系、现场勘查、高管约谈等方式对行业机构数据统计的内容进行逐一核实，并要求机构法定代表人或高级管理人员等对核实后的信息进行签字确认，做到对本地区网贷机构基本信息进行充分摸底排查，实现“一户一档”。</p>
      <p>（四）排查内容</p>
      <p>各省级人民政府对本地区机构的排查主要包括：一是网贷机构基本情况，包括但不限于股东或出资人、实际控制人、法定代表人、注册资本、借贷余额、出借人总数、分支机构数量及分布等。二是网贷机构各类产品及业务运营情况，包括产品期限、综合收益率、逾期率等。三是网贷机构存在的主要问题，包括但不限于机构是否存在设立资金池、自融、向出借人提供担保或者承诺保本保息、大规模线下营销、误导性宣传、虚构借款人及标的、发放贷款、期限拆分、发售银行理财和券商资管等产品、违规债权转让、参与高风险证券市场融资或利用类HOMS等系统从事股票市场场外配资行为、从事股权众筹或实物众筹等；是否存在信息披露不完整、不客观、不及时；是否未实行出借人资金第三方存管等问题。此外，对近年业务扩张过快、在媒体过度宣传、承诺高额回报、涉及房地产配资或校园网贷等业务的网贷机构进行重点排查。根据排查结果汇总本地区问题机构总体数量、各类问题机构的占比等，并据此对本地区机构风险状况进行判断。</p>
      <p>对于跨区域经营的网贷机构，银监会协调相关省级人民政府加强合作，密切配合，进一步增强摸底排查的完整性、准确性、时效性。</p>
      <p>三、明确标准、分类施策</p>
      <p>（一）分类处置标准</p>
      <p>专项整治工作的重点是整治和取缔互联网企业在线上线下违规或超范围开展网贷业务，以网贷名义开展非法集资等违法违规活动。分类处置标准以《指导意见》和有关监管要求等作为主要依据：一是网贷机构满足信息中介的定性。二是业务符合直接借贷的标准，即个体与个体之间通过互联网机构实现的直接借贷。三是不得触及业务“红线”，即设立资金池、自融、向出借人提供担保或者承诺保本保息、大规模线下营销、误导性宣传、虚构借款人及标的、发放贷款、期限拆分、发售银行理财和券商资管等产品、违规债权转让、参与高风险证券市场融资或利用类HOMS等系统从事股票市场场外配资行为、从事股权众筹或实物众筹等。四是落实出借人及借款人资金第三方存管要求。五是信息披露完整、客观、及时，并且具备合规的网络安全设施。</p>
      <p>（二）分类处置措施</p>
      <p>对各类网贷机构认真甄别，根据风险程度、违法违规性质和情节轻重、社会危害程度大小、处理方式等因素，准确分类，及时纠偏，制定差别化措施，防范处置风险的风险，确保风险全面排查、问题全面整治和监管全面覆盖。</p>
      <p>根据以上标准将网贷机构划分为三类，并实施分类处置。一是合规类。该类机构严格遵守信息中介定位，稳健经营、运作规范，具有较强的管理技术和风险控制能力，基本符合《指导意见》规定，未违反有关法律法规和规章制度。应对此类机构实施持续监管，支持鼓励其合规发展，督促其规范运营。二是整改类。该类机构大多数运行不规范，风险控制不足，缺乏持续经营能力和自我约束能力，大多异化为信用中介，存在触及业务“红线”的问题。此类机构应按照有关要求限期整改，整改不到位的，责令继续整改或淘汰整合，并依法予以处置。三是取缔类。此类机构涉嫌从事非法集资等违法违规活动，应对其严厉打击，坚决实施市场退出，并按照有关法律法规和规章制度规定，由相关部门给予行政处罚或依法追究刑事责任，政府不承担兜底责任。同时，做好核实资本和财务状况工作，妥善处理债权债务关系，依法保护投资者合法权益。</p>
      <p>四、职责分工</p>
      <p>按照《互联网金融风险专项整治工作实施方案》要求，专项整治工作按照银监会会同中央有关部门与省级人民政府双负责制的原则，明确分工，落实责任。</p>
      <p>（一）加强组织领导。银监会会同中央宣传部、中央维稳办、发展改革委、工业和信息化部、公安部、财政部、住房城乡建设部、人民银行、工商总局、法制办、国家网信办、国家信访局、最高人民法院、最高人民检察院成立网贷风险专项整治工作领导小组，银监会为组长单位，工业和信息化部、公安部、国家网信办、工商总局为副组长单位，其他部门为成员单位，网贷风险专项整治工作小组办公室设在银监会。</p>
      <p>（二）中央监管部门职责。银监会作为网贷风险专项整治工作统筹部门，负责总体工作的组织和协调。一是制定规则，即制定网贷行业监管制度和第三方存管等系列配套制度，拟定网贷风险专项整治工作实施方案，明确专项整治工作目标、原则、内容、措施等。二是培训部署，即对专项整治工作进行周密部署，组织开展培训。三是划清界限，即明确网贷业务负面清单，划清网贷机构不得从事的业务边界。四是督导汇总，即加强跨部门、跨地区间协调，研究重大问题、汇总工作报告等。五是在省级人民政府统一领导下，省金融办（局）与银监会省级派出机构共同牵头负责本地区分领域整治工作，共同承担分领域整治任务。</p>
      <p>各相关部门发挥职能作用，密切协作，互通信息，共享资源，形成合力。</p>
      <p>（三）各省级人民政府职责。各省级人民政府按照中央监管部门的统一方案和要求，负责本地区具体整治工作。在各省级人民政府统一领导下，设网贷风险专项整治联合工作办公室，由省金融办（局）和银监会省级派出机构共同负责，办公室成员由省级人民政府根据工作需要确定相关部门组成，具体组织实施专项整治工作，并建立风险事件应急制度和处置预案，做好本地区维稳工作，最大限度预防和减少风险事件造成的不良社会影响，维护社会稳定。</p>
      <p>五、时间进度</p>
      <p>（一）部署培训阶段。根据《互联网金融风险专项整治工作实施方案》要求，银监会协调有关各方汇总网贷行业机构基本数据统计，部署培训各地方开展专项整治工作。此项工作于2016年4月底前完成。</p>
      <p>（二）行业摸底排查阶段。各省级人民政府依照网贷行业机构基本数据统计对本地区机构进行摸底排查，并报银监会。此项工作于2016年7月底前完成。</p>
      <p>（三）分类处置阶段。各省级人民政府依照摸底排查结果，结合《指导意见》和本方案要求，对本地区机构进行分类处置。此项工作于2016年11月底前完成。</p>
      <p>（四）总结督导阶段。银监会将适时赴各地对专项整治工作进行督导，各省级人民政府应对检查、查处、整改情况进行总结，形成报告报送银监会。银监会将根据各地情况，形成规范整治工作总体报告，报送互联网金融风险专项整治工作领导小组办公室。此项工作于2017年1月底前完成。</p>
      <p>六、配套支持措施</p>
      <p>（一）加强舆论宣传引导。加强网贷风险专项整治工作正面宣传与舆论引导，鼓励网贷机构在依法合规的前提下创新发展。通过以案说法，厘清合法和非法的界限，适时主动发声，及时回应投资者关切。加强舆情监测，强化媒体责任，为整治工作营造良好的舆论环境。</p>
      <p>（二）加强各方协调配合。加强各部门沟通协调，完善工作机制，坚持部门间和区域间纵横联动，协作配合。加强中央与地方金融监管协同配合，共同履行好监管职责，形成专项整治和日常监管的合力，确保中央和地方金融监管目标和规则的一致性，守住不发生系统性区域性金融风险的底线。</p>
      <p>（三）注重工作方式方法。专项整治工作具有政策性强、涉及面广、敏感度高、难度较大等特点，要讲究整治策略，注意方式方法，做好风险隔离，依法依规，有节有度，妥善化解各类存量风险，防范风险蔓延和叠加，切实防范处置风险的风险，依法保护投资者合法权益，维护正常的经济金融秩序和社会稳定。</p>
