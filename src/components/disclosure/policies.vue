      <p>1.1数据按月披露的，统计时点为统计月末最后一日24时。数据按季度披露的，统计时点为统计季度末最后一日24时。</p>
      <p>1.2信息披露货币单位为人民币“元”，保留两位以上小数；数量单位为“个”、“人”；比例统计单位“%”。</p>
